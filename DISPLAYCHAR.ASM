.MODEL SMALL
.STACK 100H
.DATA


.CODE

MAIN PROC   
    
    MOV AH, 2
    MOV DL, '?'
    INT 21H
    
    MOV AH, 1 ; TELLS THE ROUTINE THAT A USER INPUT WILL BE GIVEN AND WILL BE SAVED IN THE AL REGISTER
    INT 21H
    
    MOV BL,AL ; THE REASON WE NEEDED TO MOVE AL TO BL BECAUSE AH,2 WILL CHANGE AL
    
    ;WE MOVE THE CURSOR TO THE BEGINNING OF THE LINE BY CARRIAGE RETURN
    MOV AH, 2
    MOV DL, 0AH
    INT 21H
    
    ; THE FOLLOWING PART MOVES TO A NEW LINE
    MOV DL, 0DH
    INT 21H
    
    MOV DL,BL
    INT 21H
MAIN ENDP

END MAIN
